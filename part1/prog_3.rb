(все 3 стороны равны) и выводит результат на экран. 

Подсказка: чтобы воспользоваться теоремой Пифагора, нужно сначала найти самую длинную 
сторону (гипотенуза) и сравнить ее значение в квадрате с суммой квадратов двух остальных 
сторон. Если все 3 стороны равны, то треугольник равнобедренный и равносторонний, но не 
прямоугольный.
