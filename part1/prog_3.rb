if (a==b)&&(b==c)
	puts "Треугольник равносторонний"
else
	puts "Треугольник равнобедреный" if (a==b)||(a==c)||(b==c)

	tr = [a, b, c].sort #Отсортируем массив по возрастанию чтобы найти гипотенузу
	puts "Треугольник прямоугольный" if ( tr[2]**2) == (tr[0]**2 + tr[1]**2)
